package Web_Services;

public class MetroLine {

    private int line_id;
    private int line_code;
    private String line_name;
    private String line_description;
    private String line_origin;
    private String line_destination;

    public int getLine_id() {
        return line_id;
    }

    public void setLine_id(int line_id) {
        this.line_id = line_id;
    }

    public int getLine_code() {
        return line_code;
    }

    public void setLine_code(int line_code) {
        this.line_code = line_code;
    }

    public String getLine_name() {
        return line_name;
    }

    public void setLine_name(String line_name) {
        this.line_name = line_name;
    }

    public String getLine_description() {
        return line_description;
    }

    public void setLine_description(String line_description) {
        this.line_description = line_description;
    }

    public String getLine_origin() {
        return line_origin;
    }

    public void setLine_origin(String line_origin) {
        this.line_origin = line_origin;
    }

    public String getLine_destination() {
        return line_destination;
    }

    public void setLine_destination(String line_destination) {
        this.line_destination = line_destination;
    }
}
